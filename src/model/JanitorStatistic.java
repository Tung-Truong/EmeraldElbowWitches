package model;

public class JanitorStatistic {

    // Attributes
    int numOfSupplies;


    // Constructors
    public JanitorStatistic(int numOfSupplies){
        this.numOfSupplies = numOfSupplies;
    }

    // getters--------------------------------------
    public int getNumOfSupplies() {
        return numOfSupplies;
    }

    // Setters
    public void setNumOfSupplies(int numOfSupplies) {
        this.numOfSupplies = numOfSupplies;
    }

}
